export const getTranslation = (lang: 'en' | 'es', key: TranslationKey, substitutions?: Record<string, string>): string => {
  const translationsForLang = translations[lang] || translations['es']; 
  let translation = translationsForLang[key] || translations['es'][key] || key.toString();
  
  if (substitutions) {
    Object.entries(substitutions).forEach(([subKey, subValue]) => {
      translation = translation.replace(new RegExp(`{{${subKey}}}`, 'g'), subValue);
    });
  }
  return translation;
};
